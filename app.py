def prep_spc(df, north, south):
    tmp = df.copy()
    tmp["value"] = tmp[[north, south]].mean(axis=1)
    return tmp.groupby("Ë£ΩÈÄ†ÊâπËôü", as_index=False).agg(
        Time=("Time", "min"),
        value=("value", "mean")
    )

def prep_lab(df, col):
    return df.groupby("Ë£ΩÈÄ†ÊâπËôü", as_index=False).agg(
        Time=("Time", "min"),
        value=(col, "mean")
    )

